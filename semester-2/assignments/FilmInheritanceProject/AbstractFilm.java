abstract public class AbstractFilm implements Film {

    abstract public void setName(){}
    abstract public void setProducer(){}
    abstract public void setDirector(){}
    abstract public void setYear(){}
    abstract public void setActors(){}
    abstract public void setLength(){}

    abstract public String getName(){}
    abstract public String getProducer(){}
    abstract public String getDirector(){}
    abstract public int getYear(){}
    abstract public String[] getActors(){}
    abstract public double getLength(){}

}